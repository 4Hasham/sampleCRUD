package com.grocery.beans;

public class search {
	private String text;
	private int id;
	private String name;
	
	public void settext(final String text) {
		this.text = text;
	}
	
	public String gettext() {
		return this.text;
	}
	
	public void setid(int id) {
		this.id = id;
	}
	
	public int getid() {
		return this.id;
	}
	
	public void setname(final String name) {
		this.name = name;
	}
	
	public String getname() {
		return this.name;
	}
}
