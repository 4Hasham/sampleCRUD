package com.grocery.beans;

public class products {
	private int id;
	private String name;
	private String price;
	
	public void setid(int id) {
		this.id = id;
	}
	
	public int getid() {
		return this.id;
	}
	
	public void setname(String name) {
		this.name = name;
	}
	
	public String getname() {
		return this.name;
	}
	
	public void setprice(String price) {
		this.price = price;
	}
	
	public String getprice() {
		return this.price;
	}
}
